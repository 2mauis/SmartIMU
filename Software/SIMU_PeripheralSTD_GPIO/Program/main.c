int main( void )
{
  HAL_InitTick();

  SIMU_GPIO_Config();

  while(1) {
    LED_R_Toggle();
    delay_ms(100);
    LED_G_Toggle();
    delay_ms(100);
    LED_B_Toggle();
    delay_ms(100);
    while(KEY_Read()) {
      LED_R_Toggle();
      LED_G_Toggle();
      LED_B_Toggle();
      delay_ms(50);
    }
  }
}

